<script>
   var adjustStripPosition = function(){
   	var $header = $("header");
   	var isHeaderFixed = $header.css("position") === "fixed";
   	var $demoHeader = $("#demoHeader");
   	var demoHeaderHeight = $demoHeader.height();
   	var isDemoHeaderVisible = $demoHeader.length && $demoHeader.css("display") !== "none";
   
   	$(".strip-element").each(function(){
   		var $el = $(this);
   		var $parent = $el.parent();
   		if ($parent.hasClass("container") || $parent.hasClass("container-wrapper")) {
   			var $section = $el.parents(".section");
   			$el.appendTo($section);
   		}
   		else if (!$parent.hasClass("section")) {
   			if (isHeaderFixed) {
   				if (parseInt($el.css("top")) < $header.outerHeight()) {
   					$(this).appendTo("header");
   				}
   				else if (isDemoHeaderVisible) {
   					var top = $el.data("top");
   					if (typeof top === "undefined") {
   						top = parseInt($el.css("top"));
   						$el.data("top", top);
   					}
   					$el.css("top", top + demoHeaderHeight);
   				}
   			}
   			else {
   				$el.appendTo("#body-wrapper");
   				if (isDemoHeaderVisible) {
   					var top = $el.data("top");
   					if (typeof top === "undefined") {
   						top = parseInt($el.css("top"));
   						$el.data("top", top);
   					}
   					$el.css("top", top + demoHeaderHeight);
   				}
   			}
   		}
   	});
   };
   
   var adjustPageHeader = function(){
   	var $demoHeader = $("#demoHeader");
   	var demoHeaderHeight = $demoHeader.height();
   	var isDemoHeaderVisible = $demoHeader.length && $demoHeader.css("display") !== "none";
   	if (isDemoHeaderVisible) {
   		var $header = $("header");
   		if ($header.css("position") === "fixed") {
   			$header.css("top", demoHeaderHeight);
   			$("#body-wrapper").css({
   				paddingTop: demoHeaderHeight + $header.height()
   			});
   			$(".mobileMenu-element .mobile-menu").css({
   				top: demoHeaderHeight
   			});
   		}
   		else {
   			$("#body-wrapper").css({
   				paddingTop: demoHeaderHeight
   			});
   			$(".mobileMenu-element .mobile-menu").css({
   				top: demoHeaderHeight
   			});
   		}
   	}
   };
   
   
   $(function(){
   	
   		if (top !== self) {
   			$("#demoHeader").hide();
   		}
   		adjustPageHeader();
   		adjustStripPosition();
   	
   
   	var $menu = $("#menu");
   	if (!$menu.find(".nav > li").length) {
   		$menu.hide();
   	}
   });
</script>
